export type Question = typeof allQuestions[0];

export type MaturityRating = {
  title: string;
  roast: string;
  emoji: string;
};

export function getRandomQuestions(numQuestions: number): Question[] {
  const shuffled = [...allQuestions].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, numQuestions);
}

export function calculateMaturityRating(selectedScores: number[]): { score: number; rating: MaturityRating } {
  const totalScore = selectedScores.reduce((sum, current) => sum + current, 0);
  const averageScore = totalScore; // Sum of scores is the total score for now
  const roundedScore = parseFloat(averageScore.toFixed(2));

  let rating: MaturityRating;

  // Certified Thantha above 7 points
  if (roundedScore >= 7) {
    rating = {
      title: "Certified Thantha",
      emoji: "👴",
      roast: `Congratulations, you magnificent sage! With a score of ${roundedScore}, you've officially reached peak elder energy. Your wisdom is so profound, it probably comes with its own set of unsolicited advice and a permanent urge to tell youngsters to get off your lawn. We salute your Thantha-ness!`,
    };
  } else if (roundedScore >= 4) { // 4-6 points
    rating = {
      title: "UPCOMING THANTHA",
      emoji: "🍵",
      roast: `Ah, the Wannabe Elder! With a score of ${roundedScore}, you've got some wisdom brewing, like a tea bag that's been steeped for a good minute – not too strong, not too weak. You're learning, you're growing, and you're almost ready to start giving unsolicited advice at family gatherings. Keep at it, young grasshopper!`,
    };
  } else { // 0-3 points
    rating = {
      title: "KUNU VAVA",
      emoji: "🍼",
      roast: `Bless your cotton socks, you adorable Baby Goat! With a score of ${roundedScore}, you're still figuring life out, and that's perfectly fine. Your wisdom is currently in its infancy, probably still learning to chew cud. Don't worry, with enough life experience (and maybe a few more grey hairs), you'll get there!`,
    };
  }

  return { score: roundedScore, rating };
}

